public class Himothy {
    
    int Energy;

    int score;

    int rowPos;

    int colPos;

    int currentTilePosition;

    Himothy(int Energy, int score, int currentTilePosition, int rowPos, int colPos)
    {
        this.Energy = Energy;
        this.score = score;
        this.currentTilePosition = currentTilePosition;
        this.rowPos = rowPos;
        this.colPos = colPos;
    }
}
